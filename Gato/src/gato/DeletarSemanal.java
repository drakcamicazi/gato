    private void deletar(int cod) {
        int n = JOptionPane.showConfirmDialog(null, "Deseja mesmo deletar o evento " + cod + "?", "Confirmação", JOptionPane.YES_NO_OPTION);

        if (n == YES_OPTION) {
            String url = "jdbc:mysql://localhost/gato?useSSL=false", usuario = "root", senha = "root";
            Connection conexao;
            PreparedStatement pstm;
            try {
                Class.forName("com.mysql.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(DeletarSemanal.class.getName()).log(Level.SEVERE, null, ex);
            }

            try {
                conexao = DriverManager.getConnection(url, usuario, senha);
                pstm = conexao.prepareStatement("delete from Evento_Semanal where pk_evento_semanal = ?");

                pstm.setInt(1, cod);

                pstm.execute();
                pstm.close();
                conexao.close();

                JOptionPane.showMessageDialog(null, "Evento " + cod + " deletado com sucesso!");
            } catch (HeadlessException | SQLException excp) {
                JOptionPane.showMessageDialog(null, "Erro ao salvar.");
                System.err.println(excp);
            }

            DeletarSemanal.this.dispose();
            DeletarSemanal ds = new DeletarSemanal();
            ds.setVisible(true);
        }
    }

    private void popularEventos() {
        String url = "jdbc:mysql://localhost/gato?useSSL=false", usuario = "root", senha = "root";
        Connection conexao;
        Statement stm;
        ResultSet rs;
        int tam = 0;
        try {
            Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DeletarSemanal.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            conexao = DriverManager.getConnection(url, usuario, senha);
            stm = conexao.createStatement();
            rs = stm.executeQuery("select pk_evento_semanal, titulo, dia_semana, hora_inicio from evento_semanal order by dia_semana asc, hora_inicio asc");

            while (rs.next()) {
                tam++;
                int cod = rs.getInt("pk_evento_semanal");
                JLabel l = new JLabel(cod + "- " + rs.getString("titulo") + ": " + rs.getString("dia_semana") + ", " + rs.getString("hora_inicio"));
                l.addMouseListener(new java.awt.event.MouseAdapter() {
                    @Override
                    public void mouseClicked(java.awt.event.MouseEvent evt) {
                        deletar(cod);
                    }
                });
                jPanel1.add(l);
            }
        } catch (HeadlessException | SQLException excp) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar do banco.");
            System.err.println(excp);
        }

        jPanel1.setLayout(new GridLayout(tam, 1));

    }

